package io.bitdrift.capture.utils

import io.bitdrift.capture.providers.Fields

fun Fields.toStringMap(): Map<String, String> {
    val map = mutableMapOf<String, String>()
    for (i in keys.indices) {
        map[keys[i]] = values[i]
    }
    return map
}
